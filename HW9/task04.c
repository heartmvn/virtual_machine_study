#include <stdio.h>

void print_digit(char s[]) {
    int count[10] = {0};  // Массив для подсчета количества каждой цифры (0-9)
    
    // Перебираем строку и подсчитываем цифры
    for (int i = 0; s[i] != '\0'; i++) 
    {
        if (s[i] >= '0' && s[i] <= '9') 
        {
            count[s[i] - '0']++;  // Увеличиваем счетчик для найденной цифры
        }
    }
    
    // Выводим цифры, которые встречаются хотя бы один раз, в порядке возрастания
    for (int i = 0; i < 10; i++) 
    {
        if (count[i] > 0) 
        {
            printf("%d %d\n", i, count[i]);
        }
    }
}

int main()
{
    char s[1000];  // Массив для хранения строки
    
    //printf("Введите строку: ");// Ввод строки с клавиатуры
    fgets(s, sizeof(s), stdin);  // Используем fgets для безопасного ввода строки
    
    print_digit(s);// Вызов функции для вывода цифр и их количества
    
    return 0;
}

